#! python3
# Have the user input a number 
# Determine if the number is positive, negative or 0 
# 2 points

# Inputs:
# number

# Outputs:
# - "positive"
# - "negative"
# - "zero"

inputnumber = input("Enter a number: ")
a = int(inputnumber)
b = 0

if a > b:
    print("positive")

if a < b:
    print("negative")

if a == b:
    print("zero")
